export function getPeriodicQuote(quotesArray: string[]): string {
  if (!quotesArray || quotesArray.length === 0) return "";

  const currentHours = Math.floor(Date.now() / (1000 * 60 * 60));

  const periodIndex = Math.floor(currentHours / 6);

  const index = periodIndex % quotesArray.length;

  return quotesArray[index];
}

